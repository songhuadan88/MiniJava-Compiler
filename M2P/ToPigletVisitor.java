	   /**
	    * f0 -> "public"
	    * f1 -> Type()
	    * f2 -> Identifier()
	    * f3 -> "("
	    * f4 -> ( FormalParameterList() )?
	    * f5 -> ")"
	    * f6 -> "{"
	    * f7 -> ( VarDeclaration() )*
	    * f8 -> ( Statement() )*
	    * f9 -> "return"
	    * f10 -> Expression()
	    * f11 -> ";"
	    * f12 -> "}"
	    */
	   public Object visit(MethodDeclaration n, SymbolInterface argu) {
	      Object _ret=null;
	      ClassItem classitem=(ClassItem)argu;
	      appendInNewLine("");
	      n.f0.accept(this, argu);
	      n.f1.accept(this, argu);
	      n.f2.accept(this, argu);
	      FunctionItem function=classitem.SearchFunction(n.f2.f0.toString());
	      appendInNewLine(classitem.name+"_"+function.name+" [ "+function.parameterType.size()+" ] ");
	      currentTab++;
	      appendInNewLine("BEGIN");
	      currentTab++;
	      n.f3.accept(this, argu);
	      n.f4.accept(this, function);
	      n.f5.accept(this, argu);
	      n.f6.accept(this, argu);
	      n.f7.accept(this, function);
	      n.f8.accept(this, function);
	      n.f9.accept(this, argu);
	      int retIndex = (Integer)(n.f10.accept(this, function));
	      currentTab--;
	      appendInNewLine("RETURN TEMP "+retIndex);	      
	      appendInNewLine("END");
	      currentTab--;
	      n.f11.accept(this, argu);
	      n.f12.accept(this, argu);
	      return _ret;
	   }

